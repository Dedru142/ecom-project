package lesson_03;

public class ComparisonOperators {
    public static void main(String[] args) {

        System.out.println(5 + " == " + 5 + " -> " + (5 == 5));
        System.out.println(5 + " == " + 6 + " -> " + (5 == 6));
        System.out.println();

        System.out.println(5 + " != " + 6 + " -> " + (5 != 5));
        System.out.println(5 + " != " + 6 + " -> " + (5 != 6));
        System.out.println();

        System.out.println(5 + " > " + 5 + " -> " + (5 > 5));
        System.out.println(5 + " > " + 6 + " -> " + (5 > 6));
        System.out.println(6 + " > " + 5 + " -> " + (6 > 5));
        System.out.println();

        System.out.println(5 + " >= " + 5 + " -> " + (5 >= 5));
        System.out.println(5 + " >= " + 6 + " -> " + (5 >= 6));
        System.out.println(6 + " >= " + 5 + " -> " + (6 >= 5));
        System.out.println();

        System.out.println(5 + " < " + 5 + " -> " + (5 < 5));
        System.out.println(5 + " < " + 6 + " -> " + (5 < 6));
        System.out.println(6 + " < " + 5 + " -> " + (6 < 5));
        System.out.println();

        System.out.println(5 + " <= " + 5 + " -> " + (5 <= 5));
        System.out.println(5 + " <= " + 6 + " -> " + (5 <= 6));
        System.out.println(6 + " <= " + 5 + " -> " + (6 <= 5));
        System.out.println();
    }
}
